package de.ableitner.vlcapi.response.playlist;

import java.util.List;

import de.ableitner.vlcapi.helpers.Checker;

public class Playlist implements IPlaylist {
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Attributes
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	private String name;
	
	private List<IPlaylistItem> items;

	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Constructors
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	public Playlist(String name, List<IPlaylistItem> items){
		Checker.checkNullAndEmptiness(name, "name");
		Checker.checkNull(items, "items");
		this.name = name;
		this.items = items;
	}
	
	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Getters and Setters
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// override Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	@Override
	public String getName() {
		return this.name;
	}

	@Override
	public List<IPlaylistItem> getItems() {
		return this.items;
	}
	
	@Override
	public String toString(){
		String result = "Playlist   name=" + name + "   with the following items:";
		for(IPlaylistItem item : this.items){
			result += "\n" + item.toString();
		}
		return result;
	}
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// public Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// private Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
}
