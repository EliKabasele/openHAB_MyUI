package de.ableitner.vlcapi.response.playlist;

import de.ableitner.vlcapi.helpers.Checker;

public class PlaylistItem implements IPlaylistItem {
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Attributes
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	private String name;
	
	private int playlistId;
	
	private int duration;
	
	private String uri;
	
	private boolean current;
	
	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Constructors
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	public PlaylistItem(String name, int playlistId, int duration, String uri, boolean current){
		Checker.checkNullAndEmptiness(name, "name");
		Checker.checkIntegerGreater(playlistId, "playlistId", -1);
		Checker.checkIntegerGreater(duration, "duration", -2);
		Checker.checkNullAndEmptiness(uri, "uri");
		Checker.checkNull(current, "current");
		this.name = name;
		this.playlistId = playlistId;
		this.duration = duration;
		this.uri = uri;
		this.current = current;
	}
	
	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Getters and Setters
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// override Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	@Override
	public String getName() {
		return this.name;
	}

	@Override
	public int getPlaylistId() {
		return this.playlistId;
	}

	@Override
	public int getDuration() {
		return this.duration;
	}

	@Override
	public String getUri() {
		return this.uri;
	}

	@Override
	public boolean isCurrent() {
		return this.current;
	}
	
	@Override
	public String toString(){
		String result = "PlaylistItem   name=" + this.name;
		result += "   playlistId=" + this.playlistId;
		result += "   duration=" + this.duration;
		result += "   uri=" + this.uri;
		result += "   current=" + this.current;
		return result;
	}

	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// public Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// private Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
}
